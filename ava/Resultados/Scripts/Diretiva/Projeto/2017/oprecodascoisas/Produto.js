function Produto() {
}

Produto.prototype.setNomeProduto = function (nomeProduto) {
    this.nomeProduto = nomeProduto;
};

Produto.prototype.setQuantidade = function (quantidade) {
    this.quantidade = quantidade;
};

Produto.prototype.setMarca = function (marca) {
    this.marca = marca;
};

Produto.prototype.setPreco = function (preco) {
    this.preco = preco;
};
